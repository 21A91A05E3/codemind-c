#include<stdio.h>
#include<math.h>
int main()
{
    int n,d,r=0,dr,c=0,s=0,temp;
    scanf("%d",&n);
    temp=n;
    while(n)
    {
        d=n%10;
        r=(r*10)+d;
        n=n/10;
    }
    while(r)
    {
        dr=r%10;
        r=r/10;
        c=c+1;
        s=s+(pow(dr,c));
    }
    if(s==temp)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}